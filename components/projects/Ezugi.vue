<script setup>
import videojs from "video.js";
import "video.js/dist/video-js.css";
import "~/assets/css/videoplayer.css";
import { ref, onMounted } from "vue";
const videoPlayer = ref();
let player = null;
onMounted(() => {
  player = videojs(
    videoPlayer.value,
    {
      autoplay: true,
      controls: true,
      fluid: true,
      enableSmoothSeeking: true,
      sources: [
        {
          src: "https://d3s9kvub6l7pwc.cloudfront.net/ezugi-final-v3.webm",
          type: "video/webm",
        },
      ],
    },
    function onPlayerReady() {
      console.log("Player is ready!");
    }
  );

  const $window = window;
  const $videoWrap = document.querySelector('.video-wrap');
  const $video = document.querySelector('.video');
  const videoHeight = $video.offsetHeight;

  const handleScroll = () => {
    const windowScrollTop = $window.pageYOffset;
    const videoBottom = videoHeight + $videoWrap.getBoundingClientRect().top;

    if (windowScrollTop > videoBottom) {
      $videoWrap.style.height = `${videoHeight}px`;
      $video.classList.add('stuck');
    } else {
      $videoWrap.style.height = 'auto';
      $video.classList.remove('stuck');
    }
  };

  $window.addEventListener('scroll', handleScroll);

  return {
    handleScroll,
  };
});
</script>